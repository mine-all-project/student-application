<script>
    module.exports = {
        data() {
            return {
                isEdit: true,
                purchasesList: [],
                form: {
                    id: '',
                    purchasesId: '',
                    counts: '99999',
                    goods: {
                        producedTime: '',
                        shelLife: '',
                        characte: '性状',
                        majorFunction: '主要治疗咳嗽',
                        specification: '规格',
                        usages: '每日一次，每次半斤',
                        adverseReactions: '不良反应',
                        mattersNeeding: '注意事项',
                        taboo: '忌辛辣，烟酒',
                        storageEnvironment: '避光，密闭储存',
                        status: 0,
                    },
                    type: 1,
                },
            };
        },
        activated() {
            let id = this.$route.query.id
            if (!!id) {
                this.findDataById(id)
            }
            this.getPurchasesList()
        },
        mounted() {
        },
        methods: {
            dateChange(key) {
                this.form.goods[key] = new moment(this.form.goods[key]).format('YYYY-MM-DD')
            },
            findDataById(id) {
                const _this = this;
                axios.get(`/manage/findDataById?id=${id}`).then(response => {
                    const result = response.data;
                    console.log('通过api获取到的数据:', result);
                    if (result.status !== 200) {
                        this.$message.error('数据加载失败');
                        return
                    }
                    _this.form = result.data;
                }).catch(function (error) {
                    console.log('请求出现错误:', error);
                });
            },
            saveForm() {
                const _this = this;
                console.log(_this.form)
                axios.post(`/api/saveStorageInfo`, _this.form).then(response => {
                    const result = response.data;
                    console.log('通过api获取到的数据:', result);
                    if (result.status !== 200) {
                        this.$message.error('数据加载失败');
                        return
                    }
                    _this.$message.success('操作成功');
                }).catch(function (error) {
                    window.location.reload();
                    console.log('请求出现错误:', error);
                });
            },
            getPurchasesList() {
                const _this = this;
                axios.get('/api/getPurchasesList').then(response => {
                    const result = response.data;
                    console.log('通过api获取到的数据:', result);
                    if (result.status !== 200) {
                        this.$message.error('数据加载失败');
                        return;
                    }
                    _this.purchasesList = result.data;
                }).catch(function (error) {
                    console.log('请求出现错误:', error);
                });
            },
        }
    }
</script>

