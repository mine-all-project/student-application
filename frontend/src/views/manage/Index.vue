<template>
  <a-layout>
    <a-layout-header style="color: #fff">
      <a-row>
        <a-col :span="4">
          <span style="font-size: 20px">管理系统</span>
        </a-col>
        <a-col :span="3" :offset="17">
          <a-dropdown>
            <a-icon type="setting"/>
            <a-menu slot="overlay">
              <a-menu-item key="1" @click="changePassword">
                <a-icon type="user"/>
                修改密码
              </a-menu-item>
              <a-menu-item key="2" @click="logout">
                <a-icon type="user"/>
                退出登录
              </a-menu-item>
            </a-menu>
            <a-button style="margin-left: 8px"> Button
              <a-icon type="down"/>
            </a-button>
          </a-dropdown>
          <span>{{ userInfo.name }}</span>
        </a-col>
      </a-row>
    </a-layout-header>
    <a-layout>
      <a-layout-sider>
        <a-menu style="width: 200px;height: 100%" mode="inline">
          <a-sub-menu :key="menu.key" @titleClick="titleClick" v-for="menu in menus"
                      v-if="menu.children && menu.children.length">
            <span slot="title"><a-icon :type="menu.icon"/><span>{{ menu.name }}</span></span>
            <a-sub-menu :key="child.key" v-if="child.children && child.children.length" v-for="child in menu.children">
              <span slot="title"><a-icon :type="child.icon"/><span>{{ child.name }}</span></span>
              <a-menu-item :key="item.key" v-for="item in child.children" @click="clickMenu(item.url)">
                <a-icon :type="item.icon"/>
                <span>{{ item.name }}</span>
              </a-menu-item>
            </a-sub-menu>
            <a-menu-item :key="child.key" v-else @click="clickMenu(child.url)">
              <a-icon :type="child.icon"/>
              <span>{{ child.name }}</span>
            </a-menu-item>
          </a-sub-menu>
          <a-menu-item :key="menu.key" v-else @click="clickMenu(menu.url)">
            <a-icon :type="menu.icon"/>
            <span>{{ menu.name }}</span>
          </a-menu-item>
        </a-menu>
      </a-layout-sider>
      <a-layout-content class="content">
        <a-modal title="修改密码" :visible.sync="show.changePassword" width="25%" ok-text="确认" cancel-text="取消"
                 @ok="submitForm" @cancel="closeModal">
          <a-form-model ref="ruleForm" :model="form" :rules="rules" :label-col="labelCol" :wrapper-col="wrapperCol">
            <a-form-model-item ref="password" label="原密码" prop="password">
              <a-input-password v-model="form.password"/>
            </a-form-model-item>
            <a-form-model-item ref="newPassword" label="新密码" prop="newPassword">
              <a-input-password v-model="form.newPassword"/>
            </a-form-model-item>
            <a-form-model-item ref="rePassword" label="重复密码" prop="rePassword">
              <a-input-password v-model="form.rePassword"/>
            </a-form-model-item>
          </a-form-model>
        </a-modal>
        <div class="demo-drawer__content">
          <a-form v-model="urlSettings">
            <a-form-item label="点击领取跳转的网址" :label-width="formLabelWidth">
              <a-input v-model="urlSettings.url" autocomplete="off"></a-input>
              <a-input v-model="urlSettings.keyWords" disabled></a-input>
            </a-form-item>
            <a-form-item :label-width="formLabelWidth">
              <a-button @click="saveUrlForm" type="primary">确定</a-button>
            </a-form-item>
          </a-form>
        </div>
      </a-layout-content>
    </a-layout>
    <a-layout-footer></a-layout-footer>
  </a-layout>
</template>

<script>
export default {
  data() {
    return {
      formLabelWidth: '80px',
      labelCol: {span: 5},
      wrapperCol: {span: 16},
      openKeys: [],
      rules: {
        password: [
          {required: true, message: '请输入原密码', trigger: 'change'},
          {min: 8, max: 16, message: '长度为8-16位', trigger: 'change'},
        ],
        newPassword: [
          {required: true, message: '请输入新密码', trigger: 'change'},
          {min: 8, max: 16, message: '长度为8-16位', trigger: 'change'},
        ],
        rePassword: [
          {required: true, message: '请重复新密码', trigger: 'change'},
          {min: 8, max: 16, message: '长度为8-16位', trigger: 'change'},
        ],
      },
      form: {
        password: '',
        newPassword: '',
        rePassword: ''
      },

      show: {
        changePassword: false
      },
      userInfo: {
        name: ''
      },
      menus: [
        {
          key: '1',
          name: '首页管理',
          icon: 'appstore',
          url: '',
        },
      ],
      urlSettings: {
        keyWords: 'receive',
        url: '',
        id: ''
      }
    };
  },
  mounted() {
    this.getUserInfo()
    this.getUrlSettings()
  },
  methods: {
    titleClick(e) {
      console.log('titleClick', e);
    },
    changePassword() {
      this.show.changePassword = true
    },
    closeModal() {
      this.show.changePassword = false
      this.$refs.ruleForm.resetFields();
    },
    logout() {
      this.$http.logout()
    },
    clickMenu(url) {
      console.log('点击:', url)
      // this.$router.push({path: url})
    },
    getUserInfo() {
      this.$http.get('/api/user/getUserInfo').then(result => {
        if (result.status !== 200) {
          this.$message.error(result.message);
          return;
        }
        if (result.data !== null) {
          this.userInfo = result.data;
        }
      }).catch(function (error) {
        console.error('出现错误:', error);
      });
    },
    submitForm() {
      this.$refs.ruleForm.validate(valid => {
        if (valid) {
          console.info('success', this.form);
          if (this.form.newPassword !== this.form.rePassword) {
            this.$message.error('两次密码不一致');
            return
          }
          this.$http.post('/api/updatePassword', this.form).then(result => {
            if (result.status !== 200) {
              this.$message.error(result.message);
              return;
            }
            this.$message.success(result.message);
            this.closeModal()
          }).catch(function (error) {
            console.error('出现错误:', error);
          });
        } else {
          console.log('验证失败');
          return false;
        }
      })
    },
    getUrlSettings(){
      this.$http.get('/api/wx/getUrlByKeyWords?keyWords=receive').then(result => {
        if (result.status !== 200) {
          this.$message.error(result.message);
          return;
        }
        if (result.data !== null) {
          this.urlSettings = result.data;
        }
      }).catch(function (error) {
        console.error('出现错误:', error);
      });
    },
    saveUrlForm() {
      this.$http.post('/api/wx/saveUrlSettings', this.urlSettings).then(result => {
        this.$message.success(result.message);
        if (result.status !== 200) {
          this.$message.error(result.message);
          return;
        }
        this.$message.success(result.message);
      }).catch(function (error) {
        console.error('出现错误:', error);
      });
    }
  }
}
</script>

